def isValidSudoku(board) -> bool:
    rows = []
    cols = []
        
    squares = [[[] for x in range(3)] for y in range(3)] 

    for i in range(3):
        for j in range(3):
            squares[i][j] = [0] * 10

    for i in range(9):
        rows.append([0] * 10)
        cols.append([0] * 10)
    
    for i in range(9):
        for j in range(9):
            if board[i][j] == ".":
                continue

            value = int(board[i][j])

            if rows[i][value] == 1:
                return False
            if cols[j][value] == 1:
                return False
            if squares[i // 3][j // 3][value] == 1:
                return False

            rows[i][value] = 1
            cols[j][value] = 1
            squares[i // 3][j // 3][value] = 1

    return True


